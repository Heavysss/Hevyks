#Функция abs
# Функция abs возвращает абсолютное число(Абсолютное число это число у которого отсутствуют любые знаки)
print(abs(-10000))

#Функция sum
# Принимает может принимать аргументом список или кортеж
# Функция sum сумирует все числа которые ей указываются
print(sum([55,22,33,44,11,66,77,88,99]))